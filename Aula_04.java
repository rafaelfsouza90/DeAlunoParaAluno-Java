package aulas;

//Criando a Classe
public class Aula_04{
    
    //Criando o método principal
    public static void main(String[]args){
        
        //Declaração da variável x que pode ARMAZENAR valores do TIPO INTEIRO(int)
        int x;
        //Declaração do valor inteiro para x
        x=20;
        
        //Exibe na tela do usuário o CONTEÚDO da vatiável x, que no caso é 20
        //Símbulo + faz a concatenação, ou seja, pode unir o conteúdo antes ou depois da variável x. 
        System.out.println("Você tem " + x + " anos");
    }
}
