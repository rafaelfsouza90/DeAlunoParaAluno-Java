package aulas;

//Classe
public class Aula_05{
    
    //Método principal
    public static void main(String[]args){
        
        //Variáveis
        int x;
        x=20;
        
        int y=200;
        
        //Operadores Aritméticos
        int soma = x+y;
        int subtrai = x-y;
        int multiplica = x*y;
        int dividir = x/y;
        
        
        //Exibe na tela do usuário
        System.out.println("A soma de x com y: " + soma);
        System.out.println("A subtração de x com y: " + subtrai);
        System.out.println("A multiplicação de x com y: " + multiplica);
        System.out.println("A divisão de x com y: " + dividir);
    }
    
    
    
    
}